-- JOIN

1. 없어진 기록 찾기
SELECT DISTINCT OUTS.ANIMAL_ID, OUTS.NAME 
FROM ANIMAL_INS INS RIGHT OUTER JOIN ANIMAL_OUTS OUTS 
ON INS.ANIMAL_ID = OUTS.ANIMAL_ID 
WHERE INS.ANIMAL_ID IS NULL ORDER BY OUTS.ANIMAL_ID;

2. 있었는데요 없었습니다
SELECT INS.ANIMAL_ID, INS.NAME 
FROM ANIMAL_INS INS INNER JOIN ANIMAL_OUTS OUTS 
ON OUTS.ANIMAL_ID = INS.ANIMAL_ID 
WHERE OUTS.DATETIME < INS.DATETIME ORDER BY INS.DATETIME;

3. 오랜 기간 보호한 동물(1)
SELECT INS.NAME, INS.DATETIME 
FROM ANIMAL_INS INS LEFT OUTER JOIN ANIMAL_OUTS OUTS 
ON INS.ANIMAL_ID = OUTS.ANIMAL_ID 
WHERE OUTS.DATETIME IS NULL ORDER BY INS.DATETIME LIMIT 3;

4. 보호소에서 중성화한 동물
SELECT INS.ANIMAL_ID, INS.ANIMAL_TYPE, INS.NAME
FROM ANIMAL_INS INS JOIN ANIMAL_OUTS OUTS ON INS.ANIMAL_ID = OUTS.ANIMAL_ID
WHERE INS.SEX_UPON_INTAKE != OUTS.SEX_UPON_OUTCOME
ORDER BY INS.ANIMAL_ID;